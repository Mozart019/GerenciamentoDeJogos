    private void carregarJogos() {
        try {
            JogoDAO dao = new JogoDAO();
            List<JogoDTO> jogos = dao.listar();
            jComboBoxJogo.removeAllItems();
            for (JogoDTO jogo : jogos) {
                jComboBoxJogo.addItem(jogo.getIdJogo() + " - " + jogo.getTituloDoJogo() + " (" + jogo.getPlataformaDoJogo() + ")");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar jogos: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void atualizarTabela() {
        try {
            EmprestimoDAO dao = new EmprestimoDAO();
            List<EmprestimoDTO> emprestimos = dao.listar();
            tableModel.setRowCount(0);
            JogoDAO jogoDao = new JogoDAO();
            for (EmprestimoDTO emprestimo : emprestimos) {
                JogoDTO jogo = jogoDao.buscarPorId(emprestimo.getIdJogo());
                String tituloJogo = jogo != null ? jogo.getIdJogo() + " - " + jogo.getTituloDoJogo() + " (" + jogo.getPlataformaDoJogo() + ")" : "Jogo Desconhecido";
                tableModel.addRow(new Object[] {
                    emprestimo.getIdEmprestimo(),
                    tituloJogo,
                    emprestimo.getDataEmprestimo(),
                    emprestimo.getDataDevolucao()
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar tabela: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

