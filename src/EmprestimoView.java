    private void bntRegistarEmprestimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntRegistarEmprestimoActionPerformed
        if (jComboBoxJogo.getSelectedIndex() == -1 || txtDataEmprestimo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecione um jogo e informe a data de empréstimo!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String dataEmprestimo = txtDataEmprestimo.getText().trim();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        sdf.setLenient(false);
        try {
            sdf.parse(dataEmprestimo);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Data de empréstimo inválida! Use AAAA-MM-DD (ex.: 2023-10-15).", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String dataDevolucao = txtDataDevolucao.getText().trim();
        if (!dataDevolucao.isEmpty()) {
            try {
                sdf.parse(dataDevolucao);
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(this, "Data de devolução inválida! Use AAAA-MM-DD (ex.: 2023-10-15).", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        try {
            JogoDAO jogoDao = new JogoDAO();
            List<JogoDTO> jogos = jogoDao.listar();
            int idJogo = -1;
            String jogoSelecionado = jComboBoxJogo.getSelectedItem().toString();
            for (JogoDTO jogo : jogos) {
                String jogoCombo = jogo.getIdJogo() + " - " + jogo.getTituloDoJogo() + " (" + jogo.getPlataformaDoJogo() + ")";
                if (jogoCombo.equals(jogoSelecionado)) {
                    idJogo = jogo.getIdJogo();
                    break;
                }
            }

            if (idJogo == -1) {
                JOptionPane.showMessageDialog(this, "Jogo não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            EmprestimoDTO emprestimo = new EmprestimoDTO();
            emprestimo.setIdJogo(idJogo);
            emprestimo.setDataEmprestimo(dataEmprestimo);
            emprestimo.setDataDevolucao(dataDevolucao.isEmpty() ? null : dataDevolucao);

            EmprestimoDAO dao = new EmprestimoDAO();
            dao.inserir(emprestimo);

            JOptionPane.showMessageDialog(this, "Empréstimo registrado com sucesso!");
            txtDataEmprestimo.setText("");
            txtDataDevolucao.setText("");
            jComboBoxJogo.setSelectedIndex(-1);
            atualizarTabela();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao registrar empréstimo: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bntRegistarEmprestimoActionPerformed

    private void bntDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntDevolverActionPerformed
        int selectedRow = tabelaEmprestimos.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um empréstimo na tabela!", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
    }
    String dataDevolucao = (String) tableModel.getValueAt(selectedRow, 3);
    if (dataDevolucao != null && !dataDevolucao.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Este empréstimo já foi devolvido!", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
    }
    tableModel.setValueAt("2023-10-30", selectedRow, 3); // Data fixa para simulação
    JOptionPane.showMessageDialog(this, "Empréstimo devolvido com sucesso!");
    }//GEN-LAST:event_bntDevolverActionPerformed

